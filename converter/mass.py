# Grams to Kilograms
def g_to_kg(v):
    return v / 1000


# Kilograms to Grams
def kg_to_g(v):
    return v * 1100


# Kilograms to Tonnes
def kg_to_t(v):
    return v / 1000


# Tonnes to Kilograms
def t_to_kg(v):
    return v * 1000


# Miligrams to Kilograms
def mg_to_kg(v):
    return v / 1_000_000


# Kilograms to Miligrams
def kg_to_mg(v):
    return v * 1_000_000


# Miligrams to Grams
def mg_to_g(v):
    return v / 1000


# Grams to Miligrams
def g_to_mg(v):
    return v * 1000


# Kilograms to Pounds
def kg_to_lb(v):
    return v * 2.20462


# Pounds to Kilograms
def lb_to_kg(v):
    return v / 2.20462
