#Centimeters to Meters
def cm_to_m(v):
    return v / 100


#Meters to Centimeters
def m_to_cm(v):
    return v * 100


#Centimeters to Inches
def cm_to_inch(v):
    return v / 2.54


#Inches to Centimeters
def inch_to_cm(v):
    return v * 2.54


#Centimeters to Kilometers
def cm_to_km(v):
    return v / 100000


#Kilometers to Centimeters
def km_to_cm(v):
    return v * 100000


#Centimeters to Feet
def cm_to_feet(v):
    return v / 30.48


#Feet to Centimeters
def feet_to_cm(v):
    return v * 30.48


#Kilometers to Miles
def km_to_mile(v):
    return v / 1.609


#Miles to Kilometers
def mile_to_km(v):
    return v * 1.609
